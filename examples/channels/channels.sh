# ပရိုဂရမ်ကို run လိုက်တဲ့အခါ `"ping"` message က 
# ကျွန်တော်တို့ channel ကနေတဆင့် goroutine
#  တစ်ခုကနေ နောက်တစ်ခုဆီကို 
# အောင်မြင်စွာ ပို့ဆောင်ခဲ့ပါတယ်။
$ go run channels.go 
ping

